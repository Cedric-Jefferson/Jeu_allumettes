package test;

public class Joueur {

	private int number;
	private String nom;
	
	public Joueur(int number, String nom) {
		super();
		this.number = number;
		this.nom = nom;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
	
}
